import React from 'react';
import { Formik, Form, Field, ErrorMessage } from 'formik';
import './App.css';


const Basic = () => (
  <div className="form">
    <h1>Login</h1>
    <Formik
      initialValues={{ email: '', password: '' }}
      validate={values => {
        let errors = {};
        if (!values.email) {
          errors.email = 'The Email is Required';
        } else if (
          !/^[A-Z0-9._%+-]+@[A-Z0-9.-]+\.[A-Z]{2,}$/i.test(values.email)
        ) {
          errors.email = 'Invalid email address';
        }
        if (!values.password) {
          errors.password = 'The password is Required';
        } else if (
          !/^[A-Z0-9]{4,10}$/i.test(values.password)
        ) {
          errors.password = 'Invalid password address';
        }
        return errors;
      }}
      onSubmit={(values, { setSubmitting }) => {
        setTimeout(() => {
          alert(JSON.stringify(values, null, 2));
          setSubmitting(false);
        }, 400);
      }}
    >
      {({ isSubmitting }) => (
        <Form>
          <label>Email : </label>
          <Field type="text" name="email" className="align" /><br />
          <ErrorMessage name="email" className="align1" component="div" /><br />
          <label>password : </label>
          <Field type="password" name="password" /><br />
          <ErrorMessage name="password" className="align1" component="div" />
          <button type="submit" className="button button1" disabled={isSubmitting}>
            Submit
          </button>
        </Form>
      )}
    </Formik>
  </div>
);

export default Basic;